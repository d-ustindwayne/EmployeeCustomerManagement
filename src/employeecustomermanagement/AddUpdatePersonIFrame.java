    private void SelectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SelectButtonActionPerformed

        String personIDText = PersonIDTextField.getText();
        int personID;
        if (personIDText.length() == 0) {
            JOptionPane.showMessageDialog(this, "Person ID cannot be empty.",
                    "Empty ID Field", JOptionPane.ERROR_MESSAGE);
            resetAll();

            // Input Validation: ID cannot be any other characters except integer
        } else if (!IndexFrame.isInt(personIDText) || 0 > Integer.parseInt(personIDText)) {
            JOptionPane.showMessageDialog(this, "Please input a valid ID number.",
                    "Invalid ID", JOptionPane.ERROR_MESSAGE);
            resetAll();

            // Proceed to put personID value if no error
        } else {
            personID = Integer.parseInt(personIDText);
            currentIDSelected = personID;
            if (AddUpdatePersonTabbedPane.getSelectedIndex() == 0) {
                boolean customerFound = false;
                for (Customer c : IndexFrame.db.CustomerList) {
                    if (personID == c.getPersonID()) {
                        CustomerNameTextField.setText(c.getName());
                        CustomerGenderComboBox.setSelectedItem(c.getGender());
                        CustomerDOBDatePicker.setDate(c.getDateOfBirth());
                        CustomerAddressTextField.setText(c.getAddress());
                        CustomerTelephoneTextField.setText(c.getTelNumber());
                        CustomerLicenseNumberTextField.setText(c.getLicenseNumber() + "");
                        CustomerLicenseTypeComboBox.setSelectedItem(toTitleCase(c.getLicenseType().toString()));
                        CustomerLicenseExpiryDatePicker.setDate(c.getLicenseExpiryDate());
                        customerFound = true;
                        break;
                    }
                }
                if (!customerFound) {
                    JOptionPane.showMessageDialog(this, "No customer found.",
                    "No Customer Found", JOptionPane.INFORMATION_MESSAGE);
                    PersonIDTextField.setText("");
                }
            } else if (AddUpdatePersonTabbedPane.getSelectedIndex() == 1) {
                boolean employeeFound = false;
                for (Employee e : IndexFrame.db.EmployeeList) {
                    if (personID == e.getPersonID()) {
                        EmployeeNameTextField.setText(e.getName());
                        EmployeeGenderComboBox.setSelectedItem(e.getGender());
                        EmployeeDOBDatePicker.setDate(e.getDateOfBirth());
                        EmployeeAddressTextField.setText(e.getAddress());
                        EmployeeTelephoneTextField.setText(e.getTelNumber());
                        EmployeeRoleComboBox.setSelectedItem(toTitleCase(e.getRole().toString()));
                        EmployeePhoneExtensionTextField.setText(e.getPhoneExt());
                        EmployeeUsernameTextField.setText(e.getUsername());
                        EmployeePasswordTextField.setText(e.getPassword());
                        employeeFound = true;
                        break;
                    }
                }
                if (!employeeFound) {
                    JOptionPane.showMessageDialog(this, "No employee found.",
                    "No Employee Found", JOptionPane.INFORMATION_MESSAGE);
                    PersonIDTextField.setText("");
                }
            }
        }

    }//GEN-LAST:event_SelectButtonActionPerformed

    /*
    Rules:
    - Fields cannot be empty
    - Date of Birth cannot be in the future
    - Customer: License Expiry Date must not be in the past
    - Customer: License Number must be unique
    - Employee: Phone Extension must be unique
     */
    private boolean validateEmployeeData() {
        boolean status = true;
        if ( // No empty field
                EmployeeNameTextField.getText().equals("")
                || EmployeeGenderComboBox.getSelectedIndex() == 0
                || EmployeeDOBDatePicker.getDate() == null
                || EmployeeAddressTextField.getText().equals("")
                || EmployeeTelephoneTextField.getText().equals("")
                || EmployeeRoleComboBox.getSelectedIndex() == 0
                || EmployeePhoneExtensionTextField.getText().equals("")
                || EmployeeUsernameTextField.getText().equals("")
                || EmployeePasswordTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Input fields cannot be empty.",
                    "Empty Fields", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        // Check existing employee by basic person details, phone extension, or username
        for (Employee e : IndexFrame.db.EmployeeList) {
            if (
                EmployeeNameTextField.getText().equals(e.getName())
                && EmployeeGenderComboBox.getSelectedItem().toString().equals(e.getGender())
                && IndexFrame.isSameDate(IndexFrame.CalendarNoHSM(EmployeeDOBDatePicker.getDate()),
                        IndexFrame.CalendarNoHSM(e.getDateOfBirth()))
                && EmployeeAddressTextField.getText().equals(e.getAddress())
                && EmployeeTelephoneTextField.getText().equals(e.getTelNumber())
            ) {
                JOptionPane.showMessageDialog(this, "This employee already exist.",
                    "Existing Employee", JOptionPane.WARNING_MESSAGE);
                return false;
            } else if (e.getPhoneExt().equals(EmployeePhoneExtensionTextField.getText())) {
                JOptionPane.showMessageDialog(this, "Phone extension number must be unique.",
                        "Phone Number Already Registered", JOptionPane.WARNING_MESSAGE);
                status = false;
            } else if (e.getUsername().equals(EmployeeUsernameTextField.getText())) {
                JOptionPane.showMessageDialog(this, "Username " + e.getUsername() + " is already in use.",
                        "Username Already Registered", JOptionPane.WARNING_MESSAGE);
                status = false;
            }
        }
        
        if (EmployeeDOBDatePicker.getDate().after(new Date())) {
            JOptionPane.showMessageDialog(this, "Date of Birth must not be in the future.",
                    "Invalid Date of Birth", JOptionPane.ERROR_MESSAGE);
            status = false;
        }

        if (!IndexFrame.isInt(EmployeePhoneExtensionTextField.getText())) {
            JOptionPane.showMessageDialog(this, "Phone Extension Number must be a whole number .",
                    "Invalid Phone Extension", JOptionPane.ERROR_MESSAGE);
            status = false;
        }
        
        return status;
    }

