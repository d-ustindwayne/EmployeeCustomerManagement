    private void ActionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActionButtonActionPerformed
        if (this.currentAction.equals("Update")) {
            if (AddUpdatePersonTabbedPane.getSelectedIndex() == 0) {
                if (currentIDSelected == 0) {
                    JOptionPane.showMessageDialog(this, "Please select a customer with their ID.",
                    "No Customer Selected", JOptionPane.WARNING_MESSAGE);
                }
                for (Customer c : IndexFrame.db.CustomerList) {
                    if (currentIDSelected == c.getPersonID()) {
                        if (
                            CustomerNameTextField.getText().length() == 0
                            || CustomerGenderComboBox.getSelectedIndex() == 0
                            || CustomerAddressTextField.getText().length() == 0
                            || CustomerTelephoneTextField.getText().length() == 0
                            || CustomerLicenseNumberTextField.getText().length() == 0
                            || CustomerLicenseTypeComboBox.getSelectedIndex() == 0
                            || CustomerLicenseExpiryDatePicker.getDate() == null
                        ) {
                           JOptionPane.showMessageDialog(this, "Field cannot be left empty.",
                                    "Empty Field", JOptionPane.WARNING_MESSAGE);
                            break;
                        } else if (
                            CustomerNameTextField.getText().equals(c.getName())
                            && CustomerGenderComboBox.getSelectedItem().toString().equals(c.getGender())
                            && CustomerAddressTextField.getText().equals(c.getAddress())
                            && CustomerTelephoneTextField.getText().equals(c.getTelNumber())
                            && CustomerLicenseNumberTextField.getText().equals(c.getLicenseNumber()+"")
                            && LicenseType.valueOf(CustomerLicenseTypeComboBox.getSelectedItem()
                                .toString().toUpperCase()).equals(c.getLicenseType())
                            && IndexFrame.isSameDate(IndexFrame.CalendarNoHSM(CustomerLicenseExpiryDatePicker.getDate()),
                                IndexFrame.CalendarNoHSM(c.getLicenseExpiryDate()))
                        ) {
                            JOptionPane.showMessageDialog(this, "Please update customer information.",
                                    "Update Information", JOptionPane.WARNING_MESSAGE);
                            break;
                        } else if (CustomerLicenseExpiryDatePicker.getDate().before(new Date())) {
                            JOptionPane.showMessageDialog(this, "License must not be expired.",
                                    "Invalid License Expiry Date", JOptionPane.ERROR_MESSAGE);
                            break;
                        }
                        
                        c.setName(CustomerNameTextField.getText());
                        c.setGender(CustomerGenderComboBox.getSelectedItem().toString().toUpperCase().charAt(0));
                        c.setAddress(CustomerAddressTextField.getText());
                        c.setTelNumber(CustomerTelephoneTextField.getText());
                        c.setLicenseType(LicenseType.valueOf(CustomerLicenseTypeComboBox.getSelectedItem().toString().toUpperCase()));
                        c.setLicenseExpiryDate(CustomerLicenseExpiryDatePicker.getDate());
                        
                        JOptionPane.showMessageDialog(this, "Successfully updated customer " + c.getName() + "'s information.",
                                "Customer Info Updated", JOptionPane.INFORMATION_MESSAGE);
                        resetAll();
                    }
                    
                }
            } else if (AddUpdatePersonTabbedPane.getSelectedIndex() == 1) {
                if (currentIDSelected == 0) {
                    JOptionPane.showMessageDialog(this, "Please select a employee with their ID.",
                    "No Employee Selected", JOptionPane.WARNING_MESSAGE);
                }
                for (Employee e : IndexFrame.db.EmployeeList) {
                    if (currentIDSelected == e.getPersonID()) {
                        if (
                            EmployeeNameTextField.getText().equals(e.getName())
                            && EmployeeGenderComboBox.getSelectedItem().toString().equals(e.getGender())
                            && EmployeeAddressTextField.getText().equals(e.getAddress())
                            && EmployeeTelephoneTextField.getText().equals(e.getTelNumber())
                            && EmployeePhoneExtensionTextField.getText().equals(e.getPhoneExt())
                            && Role.valueOf(EmployeeRoleComboBox.getSelectedItem().toString().toUpperCase()).equals(e.getRole())
                            && EmployeePasswordTextField.getText().equals(e.getPassword())
                        ) {
                            JOptionPane.showMessageDialog(this, "Please update customer information.",
                                    "Update Information", JOptionPane.WARNING_MESSAGE);
                            break;
                        } else if (
                            EmployeeNameTextField.getText().length() == 0
                            || EmployeeGenderComboBox.getSelectedIndex() == 0
                            || EmployeeAddressTextField.getText().length() == 0
                            || EmployeeTelephoneTextField.getText().length() == 0
                            || EmployeePhoneExtensionTextField.getText().length() == 0
                            || EmployeeRoleComboBox.getSelectedIndex() == 0
                            || EmployeePasswordTextField.getText().length() == 0
                        ) {
                            JOptionPane.showMessageDialog(this, "Field cannot be left empty.",
                                    "Empty Field", JOptionPane.WARNING_MESSAGE);
                            break;
                        }
                        
                        e.setName(EmployeeNameTextField.getText());
                        e.setGender(EmployeeGenderComboBox.getSelectedItem().toString().toUpperCase().charAt(0));
                        e.setAddress(EmployeeAddressTextField.getText());
                        e.setTelNumber(EmployeeTelephoneTextField.getText());
                        e.setRole(Role.valueOf(EmployeeRoleComboBox.getSelectedItem().toString().toUpperCase()));
                        e.setPhoneExt(EmployeePhoneExtensionTextField.getText());
                        e.setPassword(EmployeePasswordTextField.getText());

                        JOptionPane.showMessageDialog(this, "Successfully updated employee " + e.getName() + "'s information.",
                                "EmployeeInfo Updated", JOptionPane.INFORMATION_MESSAGE);
                        resetAll();
                    }
                }
            }
        } else if (this.currentAction.equals("Add")) {
            if (AddUpdatePersonTabbedPane.getSelectedIndex() == 0 && validateCustomerData()) {
                int newCustomerID = IndexFrame.db.CustomerList.size() + 1;
                Customer newCustomer = new Customer(
                    newCustomerID,
                    CustomerNameTextField.getText(),
                    CustomerGenderComboBox.getSelectedItem().toString().charAt(0),
                    CustomerDOBDatePicker.getDate(),
                    CustomerAddressTextField.getText(),
                    CustomerTelephoneTextField.getText(),
                    Integer.parseInt(CustomerLicenseNumberTextField.getText()),
                    LicenseType.valueOf(CustomerLicenseTypeComboBox.getSelectedItem().toString().toUpperCase()),
                    CustomerLicenseExpiryDatePicker.getDate()
                );
                IndexFrame.db.CustomerList.add(newCustomer);
                
                JOptionPane.showMessageDialog(this, "Successfully added a new customer.",
                    "New Customer Added", JOptionPane.INFORMATION_MESSAGE);
                resetAll();
            } else if (AddUpdatePersonTabbedPane.getSelectedIndex() == 1 && validateEmployeeData()) {
                int newEmployeeID = IndexFrame.db.EmployeeList.size()+1;
                Employee newEmployee = new Employee(
                        newEmployeeID,
                        EmployeeNameTextField.getText(),
                        EmployeeGenderComboBox.getSelectedItem().toString().charAt(0),
                        EmployeeDOBDatePicker.getDate(),
                        EmployeeAddressTextField.getText(),
                        EmployeeTelephoneTextField.getText(),
                        EmployeePhoneExtensionTextField.getText(),
                        Role.valueOf(EmployeeRoleComboBox.getSelectedItem().toString().toUpperCase()),
                        EmployeeUsernameTextField.getText(),
                        EmployeePasswordTextField.getText()
                );
                IndexFrame.db.EmployeeList.add(newEmployee);
                JOptionPane.showMessageDialog(this, "Successfully added a new employee.",
                    "New Employee Added", JOptionPane.INFORMATION_MESSAGE);
                resetAll();
            }
        }
    }//GEN-LAST:event_ActionButtonActionPerformed

