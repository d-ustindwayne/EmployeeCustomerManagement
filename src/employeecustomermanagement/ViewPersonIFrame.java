    private Object[] getCustomerInformation(Customer c) {
        Object[] cData = {
            c.getPersonID(),
            c.getName(),
            c.getGender(),
            c.getDateOfBirth(),
            c.getAddress(),
            c.getTelNumber(),
            c.getLicenseNumber(),
            c.getLicenseType(),
            c.getLicenseExpiryDate()
        };
        return cData;
    }

    private Object[] getEmployeeInformation(Employee e) {
        Object[] eData = {
            e.getPersonID(),
            e.getName(),
            e.getGender(),
            e.getDateOfBirth(),
            e.getAddress(),
            e.getTelNumber(),
            e.getPhoneExt(),
            e.getRole()
        };
        return eData;
    }

