    private void ViewAllButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewAllButtonActionPerformed
        PersonIDTextField.setText("");
        if (ViewPersonTabbedPane.getSelectedIndex() == 0) {
            customerTable.setRowCount(0);
            for (Customer c : IndexFrame.db.CustomerList) {
                Object[] cols = getCustomerInformation(c);
                customerTable.addRow(cols);
            }
            if (customerTable.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "List of customer is empty.",
                "No Customer Found", JOptionPane.WARNING_MESSAGE);
            }
        } else if (ViewPersonTabbedPane.getSelectedIndex() == 1) {
            employeeTable.setRowCount(0);
            for (Employee e : IndexFrame.db.EmployeeList) {
                Object[] cols = getEmployeeInformation(e);
                employeeTable.addRow(cols);
            }
            if (employeeTable.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "List of employee is empty.",
                "No Employee Found", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_ViewAllButtonActionPerformed

    private void ViewByIDButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewByIDButtonActionPerformed
        String personIDText = PersonIDTextField.getText();
        int personID;

        // Input Validation: ID cannot be empty
        if (personIDText.length() == 0) {
            JOptionPane.showMessageDialog(this, "Person ID cannot be empty.",
            "Empty ID Field", JOptionPane.ERROR_MESSAGE);
            PersonIDTextField.setText("");

        // Input Validation: ID cannot be any other characters except integer
        } else if (!IndexFrame.isInt(personIDText) || 0 > Integer.parseInt(personIDText)) {
            JOptionPane.showMessageDialog(this, "Please input a valid ID number.",
            "Invalid ID", JOptionPane.ERROR_MESSAGE);
            PersonIDTextField.setText("");

        // Proceed to put personID value if no error
        } else {
            personID = Integer.parseInt(personIDText);
            if (ViewPersonTabbedPane.getSelectedIndex() == 0) {
                customerTable.setRowCount(0);
                for (Customer c : IndexFrame.db.CustomerList) {
                    if (personID == c.getPersonID()) {
                        Object[] cols = getCustomerInformation(c);
                        customerTable.addRow(cols);
                    }
                }
                if (customerTable.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(this, "No customer found.",
                    "No Customer Found", JOptionPane.INFORMATION_MESSAGE);
                }
            } else if (ViewPersonTabbedPane.getSelectedIndex() == 1) {
                employeeTable.setRowCount(0);
                for (Employee e : IndexFrame.db.EmployeeList) {
                    if (personID == e.getPersonID()) {
                        Object[] cols = getEmployeeInformation(e);
                        employeeTable.addRow(cols);
                    }
                }
                if (employeeTable.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(this, "No employee found.",
                            "No Employee Found", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_ViewByIDButtonActionPerformed

